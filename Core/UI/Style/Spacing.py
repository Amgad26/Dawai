class Spacing:
    # Margins
    SCREEN_MARGIN  = 24   # left/right padding of every screen
    SECTION_GAP    = 10   # gap between cards in a list
    CARD_PADDING_H = 16   # horizontal padding inside a card
    CARD_PADDING_V = 14   # vertical padding inside a card
    HEADER_PADDING = 12   # topbar padding top/bottom

    # Border radius
    RADIUS_XS  = 6    # small pills, pin keys
    RADIUS_SM  = 8    # input fields, buttons
    RADIUS_MD  = 12   # medicine icon background
    RADIUS_LG  = 14   # cards, main containers
    RADIUS_XL  = 44   # phone shell (not needed in PyQt)

    # Component sizes
    AVATAR_LG  = 44   # user card avatar diameter
    AVATAR_SM  = 32   # topbar avatar (picker/list screen)
    MED_ICON   = 44   # medicine icon box size
    ICON_BTN   = 34   # settings gear button, cart button
    STEP_BTN   = 30   # stepper +/− button size
    TOGGLE_W   = 44   # toggle switch width
    TOGGLE_H   = 26   # toggle switch height
    PIN_DOT    = 14   # PIN entry dot size
    PIN_KEY_P  = 16   # PIN keypad button padding

    # Bottom bar
    BOTTOM_PAD_TOP  = 12
    BOTTOM_PAD_SIDE = 16
    BOTTOM_PAD_BOT  = 20

    # Buttons
    BTN_PADDING = 15   # primary button vertical padding
    BTN_RADIUS  = 8